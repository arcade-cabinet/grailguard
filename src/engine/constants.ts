/**
 * Base stat block for a unit archetype. Values here are the *unscaled*
 * defaults; the engine applies wave-scaling multipliers and doctrine bonuses
 * at spawn time.
 */
export interface UnitConfig {
  /** Base hit points. */
  hp: number;
  /** Movement speed in world-units per second. */
  speed: number;
  /** Base damage per attack. Negative values indicate healing (e.g. clerics). */
  damage: number;
  /** Attack/heal range in world units. */
  range: number;
  /** Seconds between attacks. */
  atkSpd: number;
  /** Whether the unit fires ranged projectiles rather than melee striking. */
  isRanged: boolean;
  /** Whether the unit heals allies instead of damaging enemies. */
  isHealer: boolean;
  /** Hex color for the 3D mesh representation. */
  color: number;
  /** Visual scale multiplier applied to the unit mesh. */
  scale: number;
  /** Wave-budget cost consumed when the wave spawner selects this unit (enemies only). */
  cost?: number;
  /** Gold rewarded to the player when this enemy unit is killed. */
  reward?: number;
  /** Default affix always applied to this unit type, if any. */
  baseAffix?: EnemyAffix;
}

