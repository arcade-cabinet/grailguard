/**
 * @module constants
 *
 * Static type definitions and data dictionaries for the Grailguard tower-defense
 * engine. Defines the full set of unit types, building types, factions, game
 * phases, spells, and enemy affixes, together with their numerical stats used
 * by the simulation in {@link GameEngine}.
 */

/**
 * Which side of the conflict an entity belongs to.
 * - `'ally'` -- player-controlled defenders
 * - `'enemy'` -- wave-spawned attackers
 */
export type Faction = 'ally' | 'enemy';

/**
 * The three high-level phases a game session cycles through.
 * - `'build'`     -- player places and upgrades structures
 * - `'defend'`    -- enemies spawn and advance toward the sanctuary
 * - `'game_over'` -- terminal state after victory or defeat
 */
export type GamePhase = 'build' | 'defend' | 'game_over';

/**
 * Identifiers for the castable spells available to the player.
 * Each spell has a faith cost and cooldown managed in {@link GameEngine}.
 */
export type SpellType =
  | 'smite'
  | 'holy_nova'
  | 'zealous_haste'
  | 'earthquake'
  | 'chrono_shift'
  | 'meteor_strike'
  | 'divine_shield';

/**
 * Modifiers that can be applied to enemy units starting at wave 6.
 * - `'armored'`       -- takes 50% reduced non-magic damage
 * - `'swift'`         -- doubled movement speed and halved attack speed
 * - `'regenerating'`  -- passively regenerates HP over time
 * - `'ranged'`        -- gains a 15-range ranged attack
 * - `'vampiric'`      -- heals for 50% of damage dealt
 * - `'explosive'`     -- deals AoE damage to nearby allies on death
 */
export type EnemyAffix = 'armored' | 'swift' | 'regenerating' | 'ranged' | 'vampiric' | 'explosive';

/**
 * All unit archetypes in the game. Ally units (`wall` through `knight`) are
 * spawned by buildings, while enemy units (`goblin` through `boss`) are
 * generated by the wave system.
 */
export type UnitType =
  | 'wall'
  | 'militia'
  | 'archer'
  | 'cleric'
  | 'knight'
  | 'goblin'
  | 'orc'
  | 'troll'
  | 'boss';

/**
 * All placeable structure types. Includes unit-spawning buildings (`hut`,
 * `range`, `temple`, `keep`), turrets (`sentry`, `obelisk`, `catapult`,
 * `sorcerer`), resource buildings (`lumber`, `mine_ore`, `mine_gem`, `vault`),
 * logistics (`track`, `mint`), and the passive `wall` barricade.
 */
export type BuildingType =
  | 'wall'
  | 'hut'
  | 'range'
  | 'temple'
  | 'keep'
  | 'sentry'
  | 'obelisk'
  | 'lumber'
  | 'mine_ore'
  | 'mine_gem'
  | 'track'
  | 'mint'
  | 'catapult'
  | 'sorcerer'
  | 'vault';

/**
 * Configuration data for a single building type. Consumed by the engine to
 * determine placement costs, spawned unit types, turret behavior, and UI
 * display metadata.
 */
export interface BuildingConfig {
  /** Gold cost to place this building. */
  cost: number;
  /** Wood cost to place this building (defaults to 0 if omitted). */
  woodCost?: number;
  /** Iron ore cost to place this building. */
  oreCost?: number;
  /** Gem cost to place this building. */
  gemCost?: number;
  /** Meta-currency cost to unlock this building in the tech tree. */
  unlockCost: number;
  /** Whether this occupies a building slot (false for walls and tracks). */
  isBuilding: boolean;
  /** If true, this building fires projectiles at enemies rather than spawning units. */
  isTurret?: boolean;
  /** Emoji icon displayed in the build palette UI. */
  icon: string;
  /** Human-readable display name. */
  name: string;
  /** Short description of the building's strategic role. */
  role: string;
  /** Stat summary string shown in the build tooltip. */
  stats: string;
  /** The unit type this building spawns, or `null` for turrets and passive buildings. */
  unit: UnitType | null;
  /** Seconds between unit spawns (or resource ticks for extractors). */
  spawnTime: number;
  /** Hex color used for the 3D mesh representation. */
  color: number;
  /** Turret attack range in world units. Only meaningful when `isTurret` is true. */
  range?: number;
  /** Turret base damage per projectile. Only meaningful when `isTurret` is true. */
  damage?: number;
  /** Turret attack cooldown in seconds. Only meaningful when `isTurret` is true. */
  atkSpd?: number;
}

